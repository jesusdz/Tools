#ifndef GAME_H
#define GAME_H

////////////////////////////////////////////////////////////////////////
// Game Types
////////////////////////////////////////////////////////////////////////

enum GameState
{
	GameStateStopped,
	GameStateStarting,
	GameStateRunning,
	GameStateStopping,
	GameStateCount,
};

struct Sprite
{
	u32 id;
	float2 pos;
	float2 size;
};

struct Game
{
	GameState state;

	Sprite character;

	float2 speed;
};

////////////////////////////////////////////////////////////////////////
// Engine -> Game interface
////////////////////////////////////////////////////////////////////////

void GameStart(Game &game);
void GameUpdate(Game &game);
void GameStop(Game &game);

////////////////////////////////////////////////////////////////////////
// Game -> Engine interface
////////////////////////////////////////////////////////////////////////

void DrawSprite(Sprite &sprite);
void DrawBox(float2 pos, float2 size, float4 color);

#endif // GAME_H
