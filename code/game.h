#ifndef GAME_H
#define GAME_H

////////////////////////////////////////////////////////////////////////
// Engine Types
////////////////////////////////////////////////////////////////////////

struct Entity;

////////////////////////////////////////////////////////////////////////
// Game Types
////////////////////////////////////////////////////////////////////////

enum GameState
{
	GameStateStopped,
	GameStateStarting,
	GameStateRunning,
	GameStateStopping,
	GameStateCount,
};

struct Input
{
	Gamepad *gamepad;
	Keyboard *keyboard;
	Mouse *mouse;
};

struct Box
{
	float2 pos;
	float2 size;
	float4 color;
};

struct Game
{
	GameState state;

	Box box1;
	float2 speed;

	Box box2;
	float2 speed2;
	float accel2;

	Entity *ent;
};

////////////////////////////////////////////////////////////////////////
// Engine -> Game interface
////////////////////////////////////////////////////////////////////////

void GameSetInput(Input input);
void GameStart(Game &game);
void GameUpdate(Game &game);
void GameStop(Game &game);

////////////////////////////////////////////////////////////////////////
// Game -> Engine interface
////////////////////////////////////////////////////////////////////////

Entity *GetEntity(const char *name);
void DrawBox(float2 pos, float2 size, float4 color);

#endif // GAME_H
